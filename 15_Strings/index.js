//Template Literals

let girl1 = "Neha";
let girl2 = "Riya";

let sentence = `${girl1} is a friend of ${girl2}`;

console.log(`${girl1} is printing a string using backticks`);
console.log(sentence);

console.log(
  `Here's an example of using both double and single quotes: "I said 'hello'".`
);

//Escape sequence Characters

let name = "Ashish'Mishra";
console.log(name); //Output: Ashish'Mishra

console.log("Hello\nWorld"); // Output: "Hello
// World"

console.log("Hello\rWorld"); // Output: "World"

console.log("Hello\tWorld"); //Output:Hello   World
